import { existsSync, readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

function loadEnvFile(path) {
  if (!existsSync(path)) return;
  for (const line of readFileSync(path, "utf8").split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    if (process.env[key] === undefined) process.env[key] = value;
  }
}

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
loadEnvFile(join(root, ".env"));
loadEnvFile(join(root, ".env.local"));

const token = (
  process.env.NEXT_PUBLIC_MAPBOX_TOKEN ?? process.env.MAPBOX_ACCESS_TOKEN
)?.trim();

if (!token) {
  console.error(
    "Missing NEXT_PUBLIC_MAPBOX_TOKEN. Set it in .env.local (see .env.local.example)."
  );
  process.exit(1);
}

const outPath = join(
  dirname(fileURLToPath(import.meta.url)),
  "..",
  "public",
  "mapbox-config.js"
);

const escaped = token.replace(/\\/g, "\\\\").replace(/"/g, '\\"');

writeFileSync(
  outPath,
  `// Generated by scripts/generate-mapbox-config.mjs — do not edit.\nwindow.MAPBOX_ACCESS_TOKEN = "${escaped}";\n`,
  "utf8"
);

console.log(`Wrote ${outPath}`);
